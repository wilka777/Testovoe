                        <h2>Что такое гостевой доступ?</h2>
                        <p>
                            Гостевой доступ в Яндекс.Директ и Google.Adwords — это возможность предоставить
                            доступ к рекламной кампании, при этом не давая доступ к аккаунту, на котором была
                            заведена рекламная кампания.
                            Таким образом, подрядчик может получить уже полностью новый аккаунт, на котором есть
                            доступ к редактированию/просмотру рекламной кампании.
                        </p>
                        <h2>Что дает гостевой доступ</h2>
                        <p>Функционал гостевого доступа позволяет:</p>

                        <ul>
                            <li>Войдите в свою учетную запись Яндекс Директ;
                            </li>
                            <li>Установить или отменить настройки (редактировать любые настройки рекламы);
                            </li>
                            <li>Оплачивать рекламу (пополнять бюджет рекламы).</li>
                        </ul>

                        <p>
                            Давайте рассмотрим интерфейс Яндекс.Директ и Google.Adwords изнутри и узнаем как
                            выдать гостевой доступ.
                        </p>

